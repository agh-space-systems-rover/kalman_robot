#include <cstdint>
#include <vector>

#include <gtest/gtest.h>

#include "kalman_arc/cobs.hpp"

TEST(CobsTest, RoundTripConversionWorks) {
	const std::vector<std::vector<uint8_t>> cases{
	    {},
	    {0},
	    {1, 2, 3},
	    {1, 0, 2, 0, 3},
	    {0, 1, 2, 3, 0},
	    std::vector<uint8_t>(254, 0x11),
	    std::vector<uint8_t>(255, 0x22),
	    {0x05, 0x0A, 0x02, 0x08, 0x01, 0x00, 0x05, 0x12, 0x02, 0x08, 0x03,
	     0x00, 0x1C, 0x1A, 0x19, 0x0A, 0x17, 0x09, 0x58, 0x39, 0xB4, 0xC8,
	     0x76, 0xBE, 0xF3, 0x3F, 0x11, 0x83, 0xC0, 0xCA, 0xA1, 0x45, 0xB6,
	     0x16, 0x40, 0x1D, 0xA1, 0xD6, 0x7C, 0x3F, 0x00, 0x18, 0x22, 0x19,
	     0x0A, 0x12, 0x09, 0x58, 0x39, 0xB4, 0xC8, 0x76, 0xBE, 0xF3, 0x3F,
	     0x11, 0x83, 0xC0, 0xCA, 0xA1, 0x45, 0xB6, 0x16, 0x40, 0x15, 0x01,
	     0x03, 0xC8, 0x42, 0x00, 0x02, 0x2A, 0x01, 0x00},
	};

	for (const auto &original : cases) {
		const auto encoded = cobs_encode(original.data(), original.size());
		const auto decoded = cobs_decode(encoded.data(), encoded.size());

		EXPECT_EQ(decoded, original);
	}
}
